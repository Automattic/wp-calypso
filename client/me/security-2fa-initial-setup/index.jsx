import { Gridicon } from '@automattic/components';
import debugFactory from 'debug';
import { localize } from 'i18n-calypso';
import PropTypes from 'prop-types';
import { Component } from 'react';
import FormButton from 'calypso/components/forms/form-button';
import FormLabel from 'calypso/components/forms/form-label';
import FormRadio from 'calypso/components/forms/form-radio';
import { gaRecordEvent } from 'calypso/lib/analytics/ga';

import './style.scss';

const debug = debugFactory( 'calypso:me:security:2fa-initial-setup' );

class Security2faInitialSetup extends Component {
	static displayName = 'Security2faInitialSetup';

	static propTypes = {
		onSuccess: PropTypes.func.isRequired,
		currentAuthMethod: PropTypes.string.isRequired,
	};

	state = {
		authMethod: 'app-based',
	};

	componentDidMount() {
		debug( this.constructor.displayName + ' React component is mounted.' );
	}

	componentWillUnmount() {
		debug( this.constructor.displayName + ' React component will unmount.' );
	}
	setAuth( event ) {
		this.setState( { authMethod: event.currentTarget.value } );
	}

	handleClick( event ) {
		gaRecordEvent( 'Me', 'Clicked On 2fa Get Started Button' );
		this.props.onSuccess( event, this.state.authMethod );
	}

	render() {
		const isAppSelected = this.props.currentAuthMethod === 'app-based';
		return (
			<div>
				<p>
					{ this.props.translate(
						'Two-Step Authentication adds an extra layer ' +
							'of security to your account. Once enabled, logging in to ' +
							'WordPress.com will require you to enter a unique passcode ' +
							'generated by an app on your mobile device or sent via text ' +
							'message, in addition to your username and password.'
					) }
				</p>

				<div>
					<FormLabel className="security-2fa-initial-setup__label">
						<FormRadio
							name="auth_method"
							value="app-based"
							defaultChecked={ isAppSelected }
							onChange={ this.setAuth.bind( this ) }
						/>
						<Gridicon icon="phone" />
						<span>Set up using an app</span>
						<p>
							Use an application on your phone to get two-factor authentication codes when you log
							in.
						</p>
					</FormLabel>

					<FormLabel className="security-2fa-initial-setup__label">
						<FormRadio
							name="auth_method"
							value="sms-settings"
							defaultChecked={ ! isAppSelected }
							onChange={ this.setAuth.bind( this ) }
						/>
						<Gridicon icon="chat" />
						<span>Set up using SMS</span>
						<p>
							WordPress.com will send you a SMS with a two-factor authentication codes when you log
							in.
						</p>
					</FormLabel>
				</div>

				<FormButton onClick={ this.handleClick.bind( this ) }>
					{ this.props.translate( 'Get Started' ) }
				</FormButton>
			</div>
		);
	}
}

export default localize( Security2faInitialSetup );
